class ArticleEntity {
  String author;
  String publishedAt;
  String content;
  String imageUrl;
  String url;
  String description;
  String title;

  ArticleEntity({
    required this.author,
    required this.publishedAt,
    required this.content,
    required this.imageUrl,
    required this.url,
    required this.description,
    required this.title,
  });
}
